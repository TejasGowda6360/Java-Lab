package myjava;

public class assignment {
public static void main(String[] args) {
	int num1 =10;
	num1 +=5;
	System.out.println("The Output of num1 += 5 is" +num1);
	num1 -=5;
	System.out.println("The Output of num1 -= 5 is" +num1);
	num1 *=5;
	System.out.println("The Output of num1 *= 5 is" +num1);
	num1 /=5;
	System.out.println("The Output of num1 /= 5 is" +num1);
	num1 %=5;
	System.out.println("The Output of num1 %= 5 is" +num1);
}
}
